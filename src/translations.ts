export function translateValue(val: string, lang: string): string {
  if (!val) return "";
  if (lang === "en") return val;
  const lower = val.toLowerCase().trim();
  const dict = lang === "te" ? teDict : lang === "hi" ? hiDict : null;
  if (!dict) return val;

  // 1. Exact match check
  if (dict[lower]) {
    return dict[lower];
  }

  // 2. Phrase translation mapping
  let result = val;
  // Sort keys by length descending to replace larger phrases first
  const sortedKeys = Object.keys(dict).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    if (key.length > 2) {
      // Escape special characters for regex
      const escapedKey = key.replace(/[-\/\\^$*+?.()|[\]{}]/g, '\\$&');
      const regex = new RegExp(`\\b${escapedKey}\\b`, 'gi');
      result = result.replace(regex, dict[key]);
    }
  }
  return result;
}

